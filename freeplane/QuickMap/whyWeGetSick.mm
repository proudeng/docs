  <body>
    <p>
      这其实是一本推广演化生物学观念的书，基于的思想基础是达尔文的<b><font color="#0000c0">自然选择理论</font></b>，它为生物体中所有的功能设计提供了理论解释。书中提到的观点在现在被人们普遍称为演化医学。
    </p>
    <p>
      沿着这个思路，探讨的核心概念是“适应”（adaptation），包括：
    </p>
    <p>
      我们为了对抗病原体而产生的适应。
    </p>
    <p>
      病原为了对抗我们的这些适应而产生的适应。
    </p>
    <p>
      我们为了这些适应付出的代价，或称，“适应失调”（maladaptation）。
    </p>
    <p>
      以及我们的机体设计与现代生活环境之间的适应失调，等等。
    </p>
    <p>
      
    </p>
