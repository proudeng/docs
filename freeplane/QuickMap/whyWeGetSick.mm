  <body>
    <p>
      自然选择的原则非常的简单和让人震惊：
    </p>
    <p>
      <b><font color="#0000c0">凡是影响到物种个体生存和繁殖的遗传差异都会受到自然的选择</font></b>。
    </p>
    <p>
      如果一个基因编码所产生的特征使下一代后裔的生存率降低，这个基因将逐渐被淘汰，直到消失。
    </p>
    <p>
      假如某种遗传突变增加了感染造成的危害，或者使个体对危险不知闪避，或者对性不感兴趣，这种突变也不会在这个物种中散播开。
    </p>
    <p>
      另一方面，如果突变的基因增加了个体对感染的抵抗力，对危险迅速闪避，并能成功地选择有生育能力的配偶，它们多半会在基因库中传播，哪怕个体要为此付出一定的代价。
    </p>
    <p>
      
    </p>
    <p>
      PS：
    </p>
    <p>
      适者生存的说法，其实生存本身并不重要，只有促进繁殖的生存才称得上是生存。
    </p>
  </body>
