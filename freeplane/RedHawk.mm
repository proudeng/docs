<node TEXT="Flow概念" ID="ID_1020712349" CREATED="1584074093355" MODIFIED="1584076557390"><richcontent TYPE="NOTE">

<html>
  <head>
    
  </head>
  <body>
    <p>
      Flow是一个逻辑通道，是一个双向的通信通道，包含有两个Transceiver实体。
    </p>
    <p>
      
    </p>
    <p>
      感觉上flow是终端和网络侧同对等对象的一个逻辑上的通道。就有点类似于TCP/IP通信，虽然实际的通信机制是上层只是跟自己的下层通信，然后通过底层跟对端通信，但是逻辑上在TCP层，通信双方都认为自己跟对方在TCP层面通信，在IP层，通信双方都认为自己跟对方在IP层面通信。
    </p>
    <p>
      flow也是这样的一个概念，网络侧和终端侧的MAC层实体可以认为自己是在跟对方的MAC实体直接通信，网络侧和终端侧的RRC实体也可以认为自己是直接跟对端的RRC实体直接通信，虽然实际的通信是最终在空口完成的。
    </p>
  </body>
</html>
</richcontent>
